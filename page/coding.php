    <h4 class="custom-headline"><i>Bachelorarbeit</i> - Synchronisierung von analogen und digitalen Scrumboards</h4>
    <div class="row">
        <div class="col-8">
            <img class="float-left coding-left" src="../img/pp.jpg">
            <p>Wie es der Titel schon erahnen lässt, ist dieses Thema komplexer und hat einen hohen Theoriebezug. Es
                geht um Scrum und entsprechende Boards. Viele Firmen nutzen sowohl das "klassische" Whiteboard mit
                Zetteln für Stories. Andere nutzen System wie JIRA, um auch mit ihren Kunden zu kommunizieren. Einige
                Firmen nutzen beides und bekommen schnell ein Problem beide Boards synchron und aktuell zu halten. Hier
                greift mein Projekt. Die Zettel auf dem Whiteboard werden durch kleine Displays mit Mikrocontrollern
                ersetzt, die Informationen aus dem Ticketsystem wie JIRA enthalten.</p>
            <p>Technisch nutze ich dazu kleine ESP8266-Controller mit e-Ink Displays, die sehr wenig Strom benötigen.
                Diese Controller kommunizieren mit dem JIRA und holen sich die aktuellsten Informationen. Mitarbeiter
                können mit den Contollern interagieren und so den Status oder den Bearbeiter der Story verändern mit
                Hilfe von verschiedenen (möglichst intuitiven) Sensoren. Meine Bachelorarbeit (und der vorangegangene
                Protoyp) wird in Zusammenarbeit mit dem Unternehmen <a href="https://www.kernpunkt.de">kernpunkt</a>
                entwickelt.</p>
